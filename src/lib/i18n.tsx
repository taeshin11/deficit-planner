function detectLang(): Lang {
  if (typeof navigator === "undefined") return "en";
  const saved = localStorage.getItem("deficitPlannerLang");
  if (saved && saved in t) return saved as Lang;
  const bl = navigator.language.split("-")[0].toLowerCase();
  return (bl in t ? bl : "en") as Lang;
}

interface I18nCtx {
  lang: Lang;
  setLang: (l: Lang) => void;
  s: (key: string) => string;
  langs: typeof LANGS;
}

const Ctx = createContext<I18nCtx>({
  lang: "en",
  setLang: () => {},
  s: (k) => k,
  langs: LANGS,
});

export function useI18n() {
  return useContext(Ctx);
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");
  const [mounted, setMounted] = useState(false);

  useEffect(() => {
    setLangState(detectLang());
    setMounted(true);
  }, []);

  const setLang = (l: Lang) => {
    setLangState(l);
    localStorage.setItem("deficitPlannerLang", l);
  };

  const s = (key: string) => t[lang]?.[key] || t.en[key] || key;

  if (!mounted) {
    // SSR / hydration: render English to avoid mismatch
    return (
      <Ctx.Provider value={{ lang: "en", setLang, s: (k) => t.en[k] || k, langs: LANGS }}>
        {children}
      </Ctx.Provider>
    );
  }

  return (
    <Ctx.Provider value={{ lang, setLang, s, langs: LANGS }}>
      {children}
    </Ctx.Provider>
  );
}
